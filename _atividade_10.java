package Java_Lista_5_Enquanto;

public class _atividade_10 {
    public static void main(String[] args) {
        int count = 1;

        while (count <= 10) {
            int result = 7 * count;
            System.out.println("7 x "+count+" = "+result);

            count = count + 1;
        }
    }
}
